    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        con=MySqlConnect.ConnectDB();
        String q1="insert into customer(cus_name,cus_usrname,cus_phone,cus_password,cus_address) values(?,?,?,?,?)";
        String q2="insert into login(login_id,password,login_type) values(?,?,'cust')";
        try{
            ps2=con.prepareStatement(q2);
            ps2.setString(1,username1.getText());
            ps2.setString(2,password1.getText());
            ps=con.prepareStatement(q1);
            ps.setString(1,name1.getText());
            ps.setString(2,username1.getText());
            ps.setString(3,phone1.getText());
            ps.setString(4,password1.getText());
            ps.setString(5,address1.getText());

            ps2.execute();
            ps.execute();
            
            name1.setText("");
            username1.setText("");
            phone1.setText("");
            password1.setText("");
            address1.setText("");
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
        showTable();
    }//GEN-LAST:event_jButtonAddActionPerformed

