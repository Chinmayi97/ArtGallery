    public void showTable(){
        Customer q=new Customer();
        ArrayList<Customer> list=q.BindTable();
        String[] columnName={"ID","FULL NAME","USERNAME","PHONE NO","PASSWORD","ADDRESS"};
        Object[][] rows=new Object[list.size()][6];
        for(int i=0;i<list.size();i++)
        {
            rows[i][0]=list.get(i).getId();
            rows[i][1]=list.get(i).getType();
            rows[i][2]=list.get(i).getTitle();
            rows[i][3]=list.get(i).getCost();
            rows[i][4]=list.get(i).getArtist();
            rows[i][5]=list.get(i).getAdd();
        } 
         
        TheModel model=new TheModel(rows,columnName);
        jTable.setModel(model);
    }
